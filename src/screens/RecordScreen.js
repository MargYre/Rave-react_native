// src/screens/RecordScreen.js - Version avec import de styles
import React, { useState, useEffect } from 'react';
import {
  View,
  Text,
  TouchableOpacity,
  FlatList,
  Alert,
  StatusBar,
  TextInput,
  Modal,
} from 'react-native';
import { Audio } from 'expo-av';
import * as FileSystem from 'expo-file-system';
import { useSelector, useDispatch } from 'react-redux';

import {
  startRecording as startRecordingAction,
  stopRecording as stopRecordingAction,
  addRecording,
  deleteRecording,
  setPlayingId,
  selectRecordings,
  selectIsRecording
} from '../store/recordingSlice';

// Import des styles s√©par√©s
import { recordStyles as styles } from './RecordScreen.styles';

export default function RecordScreen() {
  // === √âTAT REDUX ===
  const dispatch = useDispatch();
  const recordings = useSelector(selectRecordings);
  const isRecordingRedux = useSelector(selectIsRecording);
  const playingId = useSelector(state => state.recording.playingId);
  
  // === √âTAT LOCAL ===
  const [recording, setRecording] = useState(null);
  const [sound, setSound] = useState(null);
  const [recordingName, setRecordingName] = useState('');
  const [recordingDuration, setRecordingDuration] = useState(0);
  const [pendingRecordingUri, setPendingRecordingUri] = useState(null);
  const [showSaveModal, setShowSaveModal] = useState(false);
  const [permissionResponse, requestPermission] = Audio.usePermissions();

  // === CONFIGURATION AUDIO ===
  useEffect(() => {
    configureAudio();
    return () => {
      if (sound) sound.unloadAsync();
      if (recording) recording.stopAndUnloadAsync();
    };
  }, []);

  const configureAudio = async () => {
    try {
      await Audio.setAudioModeAsync({
        allowsRecordingIOS: true,
        playsInSilentModeIOS: true,
        playThroughEarpieceAndroid: false,
        staysActiveInBackground: false,
        shouldDuckAndroid: true,
      });
    } catch (error) {
      console.log('‚ùå Erreur configuration audio:', error);
    }
  };

  // === ENREGISTREMENT ===
  const startRecording = async () => {
    try {
      if (permissionResponse?.status !== 'granted') {
        console.log('üìã Demande de permission micro...');
        const permission = await requestPermission();
        if (permission.status !== 'granted') {
          Alert.alert('Permission requise', 'L\'acc√®s au microphone est n√©cessaire');
          return;
        }
      }

      console.log('üî¥ D√©marrage enregistrement...');
      
      const recordingOptions = {
        android: {
          extension: '.m4a',
          outputFormat: Audio.RECORDING_OPTION_ANDROID_OUTPUT_FORMAT_MPEG_4,
          audioEncoder: Audio.RECORDING_OPTION_ANDROID_AUDIO_ENCODER_AAC,
          sampleRate: 44100,
          numberOfChannels: 2,
          bitRate: 128000,
        },
        ios: {
          extension: '.m4a',
          outputFormat: Audio.RECORDING_OPTION_IOS_OUTPUT_FORMAT_MPEG4AAC,
          audioQuality: Audio.RECORDING_OPTION_IOS_AUDIO_QUALITY_HIGH,
          sampleRate: 44100,
          numberOfChannels: 2,
          bitRate: 128000,
        },
      };

      const { recording: newRecording } = await Audio.Recording.createAsync(recordingOptions);
      setRecording(newRecording);
      dispatch(startRecordingAction());
      
      newRecording.setOnRecordingStatusUpdate((status) => {
        if (status.isRecording) {
          setRecordingDuration(Math.floor(status.durationMillis / 1000));
        }
      });

      console.log('‚úÖ Enregistrement d√©marr√©');
    } catch (error) {
      console.log('‚ùå Erreur d√©marrage:', error);
      Alert.alert('Erreur', 'Impossible de d√©marrer l\'enregistrement');
    }
  };

  const stopRecording = async () => {
    if (!recording) return;

    try {
      console.log('‚èπÔ∏è Arr√™t enregistrement...');
      
      await recording.stopAndUnloadAsync();
      const uri = recording.getURI();
      
      setRecording(null);
      setRecordingDuration(0);
      dispatch(stopRecordingAction());

      console.log('‚úÖ Enregistrement arr√™t√©, URI:', uri);

      if (uri) {
        setPendingRecordingUri(uri);
        setRecordingName(`Enregistrement ${recordings.length + 1}`);
        setShowSaveModal(true);
      }
    } catch (error) {
      console.log('‚ùå Erreur arr√™t:', error);
      Alert.alert('Erreur', 'Probl√®me lors de l\'arr√™t');
    }
  };

  // === SAUVEGARDE ===
  const saveRecording = async () => {
    if (!pendingRecordingUri || !recordingName.trim()) {
      Alert.alert('Erreur', 'Veuillez saisir un nom pour l\'enregistrement');
      return;
    }

    try {
      console.log('üíæ Sauvegarde de l\'enregistrement...');
      
      const recordingsDir = `${FileSystem.documentDirectory}recordings/`;
      const dirInfo = await FileSystem.getInfoAsync(recordingsDir);
      
      if (!dirInfo.exists) {
        await FileSystem.makeDirectoryAsync(recordingsDir, { intermediates: true });
      }

      const fileName = `${Date.now()}_${recordingName.replace(/[^a-z0-9]/gi, '_')}.m4a`;
      const finalUri = `${recordingsDir}${fileName}`;

      await FileSystem.copyAsync({
        from: pendingRecordingUri,
        to: finalUri
      });

      const fileInfo = await FileSystem.getInfoAsync(finalUri);
      
      const newRecording = {
        id: Date.now().toString(),
        name: recordingName.trim(),
        uri: finalUri,
        duration: recordingDuration,
        size: fileInfo.size,
        createdAt: new Date().toISOString(),
      };

      dispatch(addRecording(newRecording));
      console.log('‚úÖ Enregistrement sauv√©:', newRecording);

      await deleteRecordingFile(pendingRecordingUri);
      setPendingRecordingUri(null);
      setRecordingName('');
      setShowSaveModal(false);

      Alert.alert('Succ√®s !', `Enregistrement "${recordingName}" sauvegard√©`);
    } catch (error) {
      console.log('‚ùå Erreur sauvegarde:', error);
      Alert.alert('Erreur', 'Impossible de sauvegarder l\'enregistrement');
    }
  };

  const cancelSave = async () => {
    if (pendingRecordingUri) {
      await deleteRecordingFile(pendingRecordingUri);
    }
    setPendingRecordingUri(null);
    setRecordingName('');
    setShowSaveModal(false);
  };

  // === LECTURE ===
  const playRecording = async (recordingItem) => {
    try {
      if (sound) {
        await sound.unloadAsync();
        setSound(null);
        dispatch(setPlayingId(null));
      }

      console.log('‚ñ∂Ô∏è Lecture:', recordingItem.name);
      
      const { sound: newSound } = await Audio.Sound.createAsync(
        { uri: recordingItem.uri },
        { shouldPlay: true }
      );

      setSound(newSound);
      dispatch(setPlayingId(recordingItem.id));

      newSound.setOnPlaybackStatusUpdate((status) => {
        if (status.didJustFinish) {
          dispatch(setPlayingId(null));
          newSound.unloadAsync();
          setSound(null);
        }
      });
    } catch (error) {
      console.log('‚ùå Erreur lecture:', error);
      Alert.alert('Erreur', 'Impossible de lire l\'enregistrement');
    }
  };

  const stopPlayback = async () => {
    if (sound) {
      await sound.unloadAsync();
      setSound(null);
      dispatch(setPlayingId(null));
    }
  };

  // === SUPPRESSION ===
  const handleDeleteRecording = (recordingItem) => {
    Alert.alert(
      'Supprimer l\'enregistrement',
      `Supprimer "${recordingItem.name}" ?`,
      [
        { text: 'Annuler', style: 'cancel' },
        { 
          text: 'Supprimer', 
          style: 'destructive',
          onPress: () => confirmDelete(recordingItem)
        }
      ]
    );
  };

  const confirmDelete = async (recordingItem) => {
    try {
      await deleteRecordingFile(recordingItem.uri);
      dispatch(deleteRecording(recordingItem.id));
      
      if (sound) await stopPlayback();
      
      console.log('‚úÖ Enregistrement supprim√©:', recordingItem.name);
    } catch (error) {
      console.log('‚ùå Erreur suppression:', error);
      Alert.alert('Erreur', 'Impossible de supprimer');
    }
  };

  const deleteRecordingFile = async (uri) => {
    try {
      const fileInfo = await FileSystem.getInfoAsync(uri);
      if (fileInfo.exists) {
        await FileSystem.deleteAsync(uri);
      }
    } catch (error) {
      console.log('‚ùå Erreur suppression fichier:', error);
    }
  };

  // === FORMATAGE ===
  const formatDuration = (seconds) => {
    const mins = Math.floor(seconds / 60);
    const secs = seconds % 60;
    return `${mins}:${secs.toString().padStart(2, '0')}`;
  };

  const formatFileSize = (bytes) => {
    if (bytes < 1024) return `${bytes} B`;
    if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
    return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
  };

  // === RENDU ITEM ===
  const renderRecordingItem = ({ item }) => {
    const isPlaying = playingId === item.id;
    
    return (
      <View style={styles.recordingItem}>
        <View style={styles.recordingInfo}>
          <Text style={styles.recordingName}>{item.name}</Text>
          <Text style={styles.recordingDetails}>
            {formatDuration(item.duration)} ‚Ä¢ {formatFileSize(item.size)}
          </Text>
        </View>
        
        <View style={styles.recordingActions}>
          <TouchableOpacity
            style={[styles.actionButton, styles.playButton]}
            onPress={() => isPlaying ? stopPlayback() : playRecording(item)}
          >
            <Text style={styles.actionButtonText}>
              {isPlaying ? '‚è∏Ô∏è' : '‚ñ∂Ô∏è'}
            </Text>
          </TouchableOpacity>
          
          <TouchableOpacity
            style={[styles.actionButton, styles.deleteButton]}
            onPress={() => handleDeleteRecording(item)}
          >
            <Text style={styles.actionButtonText}>üóëÔ∏è</Text>
          </TouchableOpacity>
        </View>
      </View>
    );
  };

  // === RENDU PRINCIPAL ===
  return (
    <View style={styles.container}>
      <StatusBar barStyle="dark-content" backgroundColor="#f8f9fa" />
      
      <View style={styles.header}>
        <Text style={styles.title}>üé§ Enregistrement Audio</Text>
        <Text style={styles.subtitle}>
          {recordings.length} enregistrement{recordings.length !== 1 ? 's' : ''}
        </Text>
      </View>

      {/* Contr√¥les d'enregistrement */}
      <View style={styles.recordingControls}>
        <TouchableOpacity
          style={[
            styles.recordButton,
            isRecordingRedux && styles.recordButtonActive
          ]}
          onPress={isRecordingRedux ? stopRecording : startRecording}
        >
          <Text style={styles.recordButtonText}>
            {isRecordingRedux ? '‚èπÔ∏è STOP' : 'üî¥ REC'}
          </Text>
        </TouchableOpacity>
        
        {isRecordingRedux && (
          <Text style={styles.recordingTimer}>
            üî¥ {formatDuration(recordingDuration)}
          </Text>
        )}
      </View>

      {/* Liste des enregistrements */}
      <View style={styles.recordingsList}>
        <Text style={styles.sectionTitle}>üìÇ Mes enregistrements</Text>
        
        {recordings.length === 0 ? (
          <View style={styles.emptyState}>
            <Text style={styles.emptyStateText}>
              Aucun enregistrement pour le moment.{'\n'}
              Appuyez sur REC pour commencer !
            </Text>
          </View>
        ) : (
          <FlatList
            data={recordings}
            renderItem={renderRecordingItem}
            keyExtractor={(item) => item.id}
            showsVerticalScrollIndicator={false}
          />
        )}
      </View>

      {/* Modal de sauvegarde */}
      <Modal
        visible={showSaveModal}
        transparent={true}
        animationType="slide"
        onRequestClose={cancelSave}
      >
        <View style={styles.modalOverlay}>
          <View style={styles.modalContent}>
            <Text style={styles.modalTitle}>üíæ Sauvegarder l'enregistrement</Text>
            
            <TextInput
              style={styles.modalInput}
              value={recordingName}
              onChangeText={setRecordingName}
              placeholder="Nom de l'enregistrement"
              maxLength={50}
              autoFocus={true}
            />
            
            <View style={styles.modalButtons}>
              <TouchableOpacity
                style={[styles.modalButton, styles.cancelButton]}
                onPress={cancelSave}
              >
                <Text style={styles.cancelButtonText}>Annuler</Text>
              </TouchableOpacity>
              
              <TouchableOpacity
                style={[styles.modalButton, styles.saveButton]}
                onPress={saveRecording}
              >
                <Text style={styles.saveButtonText}>Sauvegarder</Text>
              </TouchableOpacity>
            </View>
          </View>
        </View>
      </Modal>
    </View>
  );
}